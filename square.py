
def area(a):
    """
        Находит площадь квадрата.

            Параметры:
                a (float): Сторона квадрата.

            Возвращаемое значение:
                area (float): Площадь квадрата.
    """
    return a * a


def perimeter(a):
    """
        Находит периметр квадрата.

            Параметры:
                a (float): Сторона квадрата.

            Возвращаемое значение:
                perimeter (float): Периметр квадрата.
    """
    return 4 * a
